class Addition {
    execute(firstValue, secondValue) {
        return firstValue + secondValue;
    }
}

class Subtraction {
    execute(firstValue, secondValue) {
        return firstValue - secondValue;
    }
}

class Multiplication {
    execute(firstValue, secondValue) {
        return firstValue * secondValue;
    }
}

class Division {
    execute(firstValue, secondValue) {
        return firstValue / secondValue;
    }
}

export { Addition, Subtraction, Multiplication, Division }; 